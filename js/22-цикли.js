"use strict";

// Цикл "While"

let num = 50;

while (num <= 55) {
   console.log(num);
   num++;
}; 
//виведетьться від 50 до 55 включно;

// цикл "do while"

let numm = 50;

do {
   console.log(numm);
   numm++;
}
while (numm < 55); 
//виведеться від 50 до 54 включно;

// цикл "for" - найпопулярніший


let nuum = 50;

for (let i = 1; i < 8; i++){
   console.log(nuum);
   nuum++;
}            
//виведеться від 50 до 56  включно        